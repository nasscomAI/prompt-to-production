import os
import re

# ── Config ────────────────────────────────────────────────────────────────────
INPUT_FILE  = os.path.join(os.path.dirname(__file__), "../data/policy-documents/policy_hr_leave.txt")
OUTPUT_FILE = os.path.join(os.path.dirname(__file__), "../summary_hr_leave.txt")


# ── Skills ────────────────────────────────────────────────────────────────────

def read_policy_text(filepath: str) -> str:
    """Read policy file and return raw text."""
    with open(filepath, "r", encoding="utf-8") as f:
        return f.read()


def extract_clauses(text: str) -> list:
    """
    Split policy text into numbered clauses.
    Handles formats: '1.', '1)', 'Clause 1:', 'Section 1'
    """
    # Split on lines that start with a clause marker
    pattern = r'(?=(?:Clause\s+\d+|Section\s+\d+|\d+[\.\)])\s)'
    parts = re.split(pattern, text, flags=re.IGNORECASE)
    clauses = []
    for part in parts:
        part = part.strip()
        if part:
            # Extract clause number
            num_match = re.match(r'^(Clause\s+\d+|Section\s+\d+|\d+[\.\)])', part, re.IGNORECASE)
            clause_number = num_match.group(0) if num_match else f"Clause {len(clauses)+1}"
            clauses.append({
                "clause_number": clause_number,
                "clause_text":   part
            })
    return clauses if clauses else [{"clause_number": "Full Policy", "clause_text": text}]


def summarise_clause(clause: dict) -> str:
    """
    Produce a faithful summary of a clause.
    Preserves all numbers, conditions, and mandatory language.
    """
    text = clause["clause_text"]

    # Extract all numbers/dates for validation
    numbers = re.findall(r'\b\d+[\.,]?\d*\s*(?:days?|weeks?|months?|%|percent|hours?|years?)?\b', text, re.IGNORECASE)
    conditions = re.findall(r'\b(?:only if|provided that|subject to|must|shall not|cannot|no carry|not eligible)\b[^\.]*\.?', text, re.IGNORECASE)

    # Build summary lines
    lines = [f"**{clause['clause_number']}**: {text.strip()}"]

    if numbers:
        lines.append(f"  Key values: {', '.join(set(numbers))}")
    if conditions:
        lines.append(f"  Conditions: {' | '.join(c.strip() for c in conditions[:3])}")

    return "\n".join(lines)


def write_summary_file(filepath: str, summaries: list):
    """Write clause summaries to output file."""
    with open(filepath, "w", encoding="utf-8") as f:
        f.write("HR LEAVE POLICY — FAITHFUL SUMMARY\n")
        f.write("=" * 60 + "\n")
        f.write("Generated by UC-0B PolicySummarizerAgent\n")
        f.write("Author: Gaddam Siddharth | Hyderabad\n")
        f.write("=" * 60 + "\n\n")
        for i, summary in enumerate(summaries, 1):
            f.write(f"{summary}\n")
            f.write("-" * 40 + "\n\n")


# ── Main ──────────────────────────────────────────────────────────────────────

def main():
    print(f"Reading policy: {INPUT_FILE}")

    if not os.path.exists(INPUT_FILE):
        print(f"  ⚠  File not found — creating sample policy for testing")
        os.makedirs(os.path.dirname(INPUT_FILE), exist_ok=True)
        sample = """HR Leave Policy — Hyderabad Civic Tech Org

1. Annual Leave
Employees are entitled to 20 days of annual leave per calendar year. Leave must be applied for at least 7 days in advance. Unused leave cannot be carried forward to the next year.

2. Sick Leave
Employees are entitled to 12 days of paid sick leave per year. Medical certificate must be provided for absences exceeding 2 consecutive days. Sick leave cannot be encashed.

3. Maternity Leave
Female employees are entitled to 26 weeks of paid maternity leave. This benefit is available only after completion of 80 days of service in the 12 months preceding the expected delivery date.

4. Emergency Leave
Employees may take up to 3 days of emergency leave per year for immediate family emergencies. Approval must be obtained from the reporting manager within 24 hours of taking leave.
"""
        with open(INPUT_FILE, "w") as f:
            f.write(sample)

    text = read_policy_text(INPUT_FILE)
    print(f"  → {len(text)} characters loaded")

    clauses = extract_clauses(text)
    print(f"  → {len(clauses)} clauses extracted")

    summaries = [summarise_clause(c) for c in clauses]

    write_summary_file(OUTPUT_FILE, summaries)
    print(f"\nSummary written to: {OUTPUT_FILE}")
    print(f"  → {len(summaries)} clauses summarised")


if __name__ == "__main__":
    main()
